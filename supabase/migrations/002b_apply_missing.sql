-- ─── Strength Scores ──────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS strength_scores (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  activity_id UUID NOT NULL REFERENCES activities(id) ON DELETE CASCADE,
  gym_exercise_id UUID REFERENCES gym_exercises(id) ON DELETE CASCADE,
  exercise_name TEXT NOT NULL,
  muscle_group TEXT,
  estimated_1rm_kg NUMERIC(6,2) NOT NULL CHECK (estimated_1rm_kg > 0),
  bodyweight_kg NUMERIC(5,1) CHECK (bodyweight_kg > 0),
  relative_strength NUMERIC(5,2) CHECK (relative_strength > 0),
  volume_load_kg NUMERIC(10,2) CHECK (volume_load_kg >= 0),
  strength_index INTEGER NOT NULL CHECK (strength_index >= 0 AND strength_index <= 999),
  score_breakdown JSONB NOT NULL DEFAULT '{}',
  recorded_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_strength_scores_user_time ON strength_scores(user_id, recorded_at DESC);
CREATE INDEX IF NOT EXISTS idx_strength_scores_user_exercise ON strength_scores(user_id, exercise_name, recorded_at DESC);
CREATE INDEX IF NOT EXISTS idx_strength_scores_activity ON strength_scores(activity_id);

