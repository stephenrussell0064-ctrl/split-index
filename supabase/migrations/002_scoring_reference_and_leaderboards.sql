  WITH CHECK (auth.uid() = user_id AND status = 'pending');

CREATE POLICY "Recipients can respond to friend requests" ON friends FOR UPDATE
  USING (auth.uid() = friend_id) WITH CHECK (auth.uid() = friend_id);

CREATE POLICY "Either party can remove friendship" ON friends FOR DELETE
  USING (auth.uid() = user_id OR auth.uid() = friend_id);
