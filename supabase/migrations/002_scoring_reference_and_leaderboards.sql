-- ─── Sleep Logs ───────────────────────────────────────────────────────────────
CREATE TABLE sleep_logs (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  sleep_date DATE NOT NULL,
  bed_time TIMESTAMPTZ,
  wake_time TIMESTAMPTZ,
  sleep_hours NUMERIC(4,1) NOT NULL CHECK (sleep_hours >= 0 AND sleep_hours <= 24),
  quality SMALLINT CHECK (quality >= 1 AND quality <= 5),
  notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CHECK (bed_time IS NULL OR wake_time IS NULL OR wake_time > bed_time),
  UNIQUE(user_id, sleep_date)
);

CREATE INDEX idx_sleep_logs_user_date ON sleep_logs(user_id, sleep_date DESC);

COMMENT ON COLUMN sleep_logs.quality IS 'Subjective sleep quality, 1 (poor) to 5 (excellent).';

-- ─── Leaderboards ─────────────────────────────────────────────────────────────
-- Denormalized current index on profiles avoids scanning split_index_history
-- for all-time leaderboard queries; kept in sync by trigger below.
ALTER TABLE profiles
  ADD COLUMN current_split_index INTEGER CHECK (current_split_index >= 0 AND current_split_index <= 999),
  ADD COLUMN current_endurance_index INTEGER CHECK (current_endurance_index >= 0 AND current_endurance_index <= 999),
  ADD COLUMN current_strength_index INTEGER CHECK (current_strength_index >= 0 AND current_strength_index <= 999),
  ADD COLUMN index_updated_at TIMESTAMPTZ;

CREATE INDEX idx_profiles_current_index
  ON profiles(current_split_index DESC)
  WHERE current_split_index IS NOT NULL;

CREATE OR REPLACE FUNCTION sync_profile_current_index()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE profiles SET
    current_split_index = NEW.split_index,
    current_endurance_index = NEW.endurance_index,
    current_strength_index = NEW.strength_index,
    index_updated_at = NEW.recorded_at
  WHERE user_id = NEW.user_id;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER split_index_history_sync_profile
  AFTER INSERT ON split_index_history
  FOR EACH ROW EXECUTE FUNCTION sync_profile_current_index();

-- Period leaderboards (weekly/monthly/all_time), refreshed by a scheduled job
-- (pg_cron or an edge function) using the service role — not writable by users.
-- period_start is the first day of the period (epoch date 1970-01-01 for all_time).
CREATE TABLE leaderboard_entries (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  period leaderboard_period NOT NULL,
  period_start DATE NOT NULL,
  split_index INTEGER NOT NULL CHECK (split_index >= 0 AND split_index <= 999),
  endurance_index INTEGER CHECK (endurance_index >= 0 AND endurance_index <= 999),
  strength_index INTEGER CHECK (strength_index >= 0 AND strength_index <= 999),
  rank INTEGER NOT NULL CHECK (rank > 0),
  previous_rank INTEGER CHECK (previous_rank > 0),
  computed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE(period, period_start, user_id)
);

CREATE INDEX idx_leaderboard_period_rank ON leaderboard_entries(period, period_start DESC, rank);
CREATE INDEX idx_leaderboard_user ON leaderboard_entries(user_id, period);

