-- ─── Strength Scores ──────────────────────────────────────────────────────────
-- Per-exercise strength scoring for a session, distinct from the aggregate
-- workout_scores row. One row per scored exercise within a gym activity.
CREATE TABLE strength_scores (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  activity_id UUID NOT NULL REFERENCES activities(id) ON DELETE CASCADE,
  gym_exercise_id UUID REFERENCES gym_exercises(id) ON DELETE CASCADE,
  exercise_name TEXT NOT NULL,
  muscle_group TEXT,
  estimated_1rm_kg NUMERIC(6,2) NOT NULL CHECK (estimated_1rm_kg > 0),
  bodyweight_kg NUMERIC(5,1) CHECK (bodyweight_kg > 0),
  -- estimated_1rm_kg / bodyweight_kg at time of scoring
  relative_strength NUMERIC(5,2) CHECK (relative_strength > 0),
  volume_load_kg NUMERIC(10,2) CHECK (volume_load_kg >= 0),
  strength_index INTEGER NOT NULL CHECK (strength_index >= 0 AND strength_index <= 999),
  score_breakdown JSONB NOT NULL DEFAULT '{}',
  recorded_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_strength_scores_user_time ON strength_scores(user_id, recorded_at DESC);
CREATE INDEX idx_strength_scores_user_exercise ON strength_scores(user_id, exercise_name, recorded_at DESC);
CREATE INDEX idx_strength_scores_activity ON strength_scores(activity_id);

-- ─── Reference Values (scoring standards) ─────────────────────────────────────
-- Benchmark standards used by the scoring engine to normalize raw performance
-- into a 0–999 index: e.g. expected 5k pace, or big-3 1RM/bodyweight ratios,
-- segmented by sport, gender, experience level, and optional age bracket.
-- Rows with NULL age bounds apply to all ages. Gender rows exist for
-- male/female; app logic falls back for other/prefer_not_to_say.
CREATE TABLE reference_values (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  sport sport_type NOT NULL REFERENCES sports(slug),
  metric TEXT NOT NULL,
  gender gender_type NOT NULL,
  experience experience_level NOT NULL,
  age_min SMALLINT CHECK (age_min >= 13),
  age_max SMALLINT CHECK (age_max <= 120),
  value NUMERIC(12,4) NOT NULL CHECK (value > 0),
  unit TEXT NOT NULL,
  notes TEXT,
  CHECK (age_min IS NULL OR age_max IS NULL OR age_max > age_min),
  UNIQUE NULLS NOT DISTINCT (sport, metric, gender, experience, age_min, age_max)
);

CREATE INDEX idx_reference_values_lookup ON reference_values(sport, metric, gender, experience);

